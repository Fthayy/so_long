#include <libft.h>

char	*ft_strchr(const char *s, int c)
{
	unsigned char	f;

	f = c;
	while (*s)
	{
		if (*s == f)
			return ((char *)s);
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (0);
}
